% --- Executes on button press in done.
function done_Callback(hObject, eventdata, handles)
try
    speed = get(handles.popupmenu1,'String');
    index = get(handles.popupmenu1,'Value');
    speed = speed{index};
    speed(speed=='x') = [];
    speed = str2double(speed);
    if speed < 1, speed = 1/abs(speed);end
    userData.speed = speed;
    userData.onscreenDisplay = get(handles.checkbox4,'Value');
    userData.changeFiles = get(handles.checkbox6,'Value');
    set(handles.figure1,'userData',userData);
    uiresume
catch ME
    uiresume
    errordlg2(ME.message);
    return
end

